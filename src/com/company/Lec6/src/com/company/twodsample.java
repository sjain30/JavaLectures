package com.company;

public class twodsample {
    public static void main(String[] args) {
        int ar[][]= new int[3][3];
        for (int i = 0; i <ar.length; i++) {
            for (int j = 0; j <ar[i].length; j++) {
                System.out.print(ar[i][j]);
            }
            System.out.println();
        }
    }
}
